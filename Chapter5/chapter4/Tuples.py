'''Tuples are sequence datatype in python.
We can not modify or change the tuple once it has been created.
Tuples can be represented in a paranthesis '()'
Tuples are immutable. '''

# Tuple Creation:

Z = (3,6,90,5,32,12,6,7) #Tuple with more than one element.
Z1 = () # Empty tuple
Z2 = (1) #Tuple with single element but it is wrong way to represent a tuple.
Z3 = (1,) #Tuple with single element and it is the way to represent a tuple.
print(Z)
print(Z1)
print(Z2)
print(Z3)
print(Z[1])  #  access the element using index.
print(Z[0:2])