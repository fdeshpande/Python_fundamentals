'''There are various types of methods in tuple'''

# Tuple count() :
print("----: TUPLE COUNT METHOD :----")
Z = (3,6,90,5,32,12,6,7,7)
print(Z.count(7)) # it counts the numbers of occurrence of any element.

# Tuple Index() :
print("----: TUPLE INDEX METHOD :----")
Z = (3,6,90,5,32,12,6,7,7)
print(Z.index(7)) # It gives the index of first occurence of any element.
